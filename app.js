var express = require('express');
var path = require('path');
var favicon = require('serve-favicon');
var logger = require('morgan');
var cookieParser = require('cookie-parser');
var bodyParser = require('body-parser');
var session = require('express-session');
var routes = require('./routes/index');
var users = require('./routes/users');
var projects = require('./routes/projects');
var webhooks = require('./routes/webhooks');
var exphbs  = require('express-handlebars');
var serveIndex = require('serve-index');
var config = require("./config");

var app = express();

// Populates req.session
app.use(session({
    resave: false, // don't save session if unmodified
    saveUninitialized: false, // don't create session until something stored
    secret: 'gitlab-pages'
}));

// view engine setup
app.set('views', path.join(__dirname, 'views'));
app.engine('handlebars', exphbs({defaultLayout: 'main'}));
app.set('view engine', 'handlebars');

// uncomment after placing your favicon in /public
//app.use(favicon(__dirname + '/public/favicon.ico'));
app.use(logger('dev'));
app.use(bodyParser.json());
app.use(bodyParser.urlencoded({ extended: true }));
app.use(cookieParser());
app.use(express.static(path.join(__dirname, 'public')));

//
app.use('/', routes);
app.use('/users', users);
app.use('/projects', projects);
app.use('/webhooks', webhooks);

// Pages
app.use('/pages', express.static(config.deploy.publicPagesDir));
app.use('/pages/:namespace/:project/*', function(req, res, next) {
    // Serve directory indexes for public/ftp folder (with icons)
    var p = req.originalUrl.replace(/^\/pages\//, '');
    var dir = path.join(config.deploy.publicPagesDir, p);
    var index = serveIndex(dir, {'icons': true})
    index(req, res, next);
});

// catch 404 and forward to error handler
app.use(function(req, res, next) {
    var err = new Error('Not Found');
    err.status = 404;
    next(err);
});

// error handlers

// development error handler
// will print stacktrace
if (app.get('env') === 'development') {
    app.use(function(err, req, res, next) {
        console.log(err);
        res.status(err.status || 500);
        res.render('error', {
            message: err.message,
            error: err
        });
    });
}

// production error handler
// no stacktraces leaked to user
app.use(function(err, req, res, next) {
    res.status(err.status || 500);
    res.render('error', {
        message: err.message,
        error: {}
    });
});


module.exports = app;
